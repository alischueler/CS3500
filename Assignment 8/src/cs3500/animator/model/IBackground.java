package cs3500.animator.model;

/**
 * represents a canvas.
 */
public interface IBackground {

  /**
   * gets the x bound.
   *
   * @return x bound
   */
  int getXBound();

  /**
   * gets the y bound.
   *
   * @return y bound
   */
  int getYBound();

  /**
   * gets the height.
   *
   * @return height
   */
  int getHeight();

  /**
   * gets the width.
   *
   * @return width
   */
  int getWidth();

}
